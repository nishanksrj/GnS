				<h3 class="animateColumn">Inter IIT Sports Meet</h3>
				<h4 class="animateColumn">The Inter IIT Sports Meet is the annual sports tournament of the Indian Institutes of Technology. It is organized in December every year, with the Aquatics events held separately in October. The seven old IITs - IIT Bombay, IIT Delhi, IIT Madras, IIT Kanpur, IIT Kharagpur, IIT Roorkee and IIT Guwahati - take turns hosting the event. Inter IIT Sports Meet (or just Inter IIT, as students of the IITs call it) is the one of the rare events wherein all the IITs participate.
				<br><br>
				Tournaments are held in 13 different sports, namely: Athletics, Badminton, Basketball, Cricket, Football, Hockey, Lawn Tennis, Squash, Swimming, Table Tennis, Volleyball, Water Polo, Weightlifting.
				<br><br>
				As the Inter-IIT Rule Book notes, the main objective of Inter IIT Sports Meet is “to organize sports competitions in some selected Olympics-recognized events, to encourage sportsmanship, to promote and create a better harmony, understanding and friendly interaction among the students of the IITs as well as to foster a close association among them”.
				<br><br>
It also goes on add, “to make students aware of the importance of sports and games in a modern era, to help students perform skillfully in all life situations and also raise the standard of sports in IIT’s.”</h4>
