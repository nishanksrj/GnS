				<h3 class="animateColumn">A Brief History</h3>
				<h4 class="animateColumn">The proposition for an Inter IIT Sports Meet was brought up in 1961, and IIT Bombay hosted the first ever Inter IIT Sports Meet. Only five IITs were in existence then, namely IIT Bombay, IIT Madras, IIT Kharagpur, IIT Kanpur and IIT Delhi; who competed in five sports only.
				<br><br>
				The Meet in 1971 was cancelled due to the India-Pakistan war, while the one in 1992 was to be held at IIT Bombay but was cancelled owing to the communal riots in Mumbai. The 2015 meet that was supposed to be held at IIT Madras was washed out due to unpredictable rains.
				</b></h4>
			</div>
			<div class="container animateColumn">
				<h3 class="animateColumn">Format</h3>
				<h4 class="animateColumn">Each sport carries 10 points for the winners, 6 for the runners up, 4 for the 3rd place and 2 for the 4th place. Total points for a particular IIT are summed up and the total is known as their GC tally. The IIT with maximum GC tally wins the Inter IIT Sports meet, and gets to lift the prestigious GC trophy. The dream of every member of the contingent is to perform to their best of abilities at the prestigious Inter IIT tournament and contribute to bringing the GC home. IIT Kanpur has been winning the overall General Championship for the past three years. We are looking up to <b>#MissionMadras.</b></h4>
